export default class User {
    constructor() {
        this.username = null;
        this.password = null;
        this.firstname = null;
        this.lastname = null;
        this.email = null;
        this.address = null;
        this.phone = null;
    }

    reset() {
        this.username = null;
        this.password = null;
        this.firstname = null;
        this.lastname = null;
        this.email = null;
        this.address = null;
        this.phone = null;
    }
}